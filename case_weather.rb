fav_weather = 'sunny'

case fav_weather
when 'rainy'
	puts "Uggg. Go back to bed"
when 'snowy'
	puts "Kind of okay. Go sledding."
when 'windy'
	puts "Is it sunny, too? Hopefully. "
when 'overcast'
	puts "Not my favorite. "
when 'hurricane'
	puts "HIDE!"
else
	puts "What do you call that weather?"
end
